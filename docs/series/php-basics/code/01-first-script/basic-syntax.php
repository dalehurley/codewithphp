<?php

/**
 * Basic PHP Syntax Examples
 * 
 * This script demonstrates fundamental PHP syntax:
 * - PHP opening and closing tags
 * - Different types of comments
 * - The echo statement
 * - Basic string output
 */

// Single-line comment: This is ignored by PHP

/* 
   Multi-line comment
   This can span
   multiple lines
*/

/**
 * DocBlock comment - used for documentation
 * These are typically used above functions and classes
 */

// Echo statement - outputs text
echo "Welcome to PHP!" . PHP_EOL;

// You can echo multiple things
echo "Hello", " ", "World!" . PHP_EOL;

// Or concatenate with the . operator
echo "PHP " . "is " . "awesome!" . PHP_EOL;
